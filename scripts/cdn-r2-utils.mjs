const MISSING_OBJECT_PATTERNS = [
  'NotFound',
  'No such object',
  '404',
  'The specified key does not exist.',
];

export function isMissingR2ObjectError(output) {
  return MISSING_OBJECT_PATTERNS.some((pattern) => output.includes(pattern));
}
